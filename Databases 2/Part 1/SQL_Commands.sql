DROP DATABASE IF EXISTS insurance;
CREATE DATABASE insurance;
USE insurance;
#
#Create the customer table
#
CREATE TABLE CUSTOMER(CUSTCODE INT(4) NOT NULL AUTO_INCREMENT, AFM INT(9) UNIQUE, CNAME VARCHAR(20), CSURNAME VARCHAR(20), 
	ADDRESS VARCHAR(30), CITY VARCHAR(10), TK INT(5), PHONE VARCHAR(15), DOY VARCHAR(20), 
	PRIMARY KEY(CUSTCODE));
#Start counting from 1001
ALTER TABLE customer AUTO_INCREMENT = 1001;
#
#Create the category table
#
CREATE TABLE CATEGORY(CATCODE INT(2) NOT NULL, CATNAME VARCHAR(20),
	PRIMARY KEY (CATCODE));
#
#Create the product table
CREATE TABLE PRODUCT(PRODCODE INT(3) NOT NULL, PNAME VARCHAR(20), ANNCOST INT(5), MINTIME VARCHAR(15), 
	BENEFITS VARCHAR(15), CATCODE INT(2) NOT NULL,
	PRIMARY KEY(PRODCODE),
	FOREIGN KEY(CATCODE) REFERENCES CATEGORY(CATCODE));
#
#Create the contract table
#
CREATE TABLE CONTRACT(CONCODE INT(5) NOT NULL AUTO_INCREMENT, CUSTCODE INT(4) NOT NULL, PRODCODE INT(3) NOT NULL, 
	STARTDATE DATE, ENDDATE DATE, COST INT(6),
	PRIMARY KEY (CONCODE), 
	FOREIGN KEY(CUSTCODE) REFERENCES CUSTOMER(CUSTCODE),
	FOREIGN KEY(PRODCODE) REFERENCES PRODUCT(PRODCODE));
#Start counting from 10001
ALTER TABLE contract AUTO_INCREMENT = 10001;
